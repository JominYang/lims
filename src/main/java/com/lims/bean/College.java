package com.lims.bean;

public class College {
	private int id;
	private int college_num;//学院编号号
	private String college_name;//学院名称
	private int	college_state;//学院状态
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCollege_num() {
		return college_num;
	}
	public void setCollege_num(int college_num) {
		this.college_num = college_num;
	}
	public String getCollege_name() {
		return college_name;
	}
	public void setCollege_name(String college_name) {
		this.college_name = college_name;
	}
	public int getCollege_state() {
		return college_state;
	}
	public void setCollege_state(int college_state) {
		this.college_state = college_state;
	}
	
	
}
