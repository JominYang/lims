package com.lims.bean;

public class Classes {
	private int id;
	private String class_num;
	private int class_grade;
	private int class_state;
	private int major_id;
	public final int getId() {
		return id;
	}
	public final void setId(int id) {
		this.id = id;
	}
	public final String getClass_num() {
		return class_num;
	}
	public final void setClass_num(String class_num) {
		this.class_num = class_num;
	}
	public final int getClass_grade() {
		return class_grade;
	}
	public final void setClass_grade(int class_grade) {
		this.class_grade = class_grade;
	}
	public final int getClass_state() {
		return class_state;
	}
	public final void setClass_state(int class_state) {
		this.class_state = class_state;
	}
	public final int getMajor_id() {
		return major_id;
	}
	public final void setMajor_id(int major_id) {
		this.major_id = major_id;
	}
	
}
